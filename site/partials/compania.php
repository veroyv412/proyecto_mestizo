        <h1>LA COMPAÑÍA</h1>
    </div>
    <br>
    <div class="row">
        <div class="col-xs-12">
            Colectivo artístico/danzario, producto del desarrollo de investigaciones sobre danzas y
            ritmos folclóricos cubanos y de la incursión en el estilo estético y
            coreográfico denominado afro-contemporáneo.
            <br><br>
            A través de su trabajo la compañía da cuenta de la complejidad histórico
            cultural que habita las danzas populares a lo largo y ancho de nuestro continente.
            <br><br>
            Creada por la bailarina y coreógrafa Gaby Fabro en el año 2004, como una herramienta que le
            permite explorar en profundidad la identidad afro americana.
            <br><br>
            Integrada por bailarines oriundos de distintas provincias de nuestro territorio y cubanos.
            Formados en danzas folclóricas, populares y técnicas madre, como la danza contemporánea,
            danza clásica y danza moderna.
            <br><br>
            La Compañía de Danza Proyecto Mestizo, se propone como un trabajo en constante formación,
            en continua exploración y desarrollo. Como una búsqueda y a la vez construcción en nuestra
            historia e identidad.
            <br><br>
            Con el compromiso de mejora constante en la gestión e intervenciones en la comunidad.
        </div>
