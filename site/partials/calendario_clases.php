                    <h3>Danza Moderna Cubana</h3>
                    <h6>Lunes 20hs | Jueves 20.30hs </h6>
                    <p>
                        En Cuba la danza Moderna comienza como movimiento artístico de vanguardia en 1948 y
                        ya desde 1959 el Conjunto Nacional de Danza Moderna, se desarrolla como institución
                        rectora de esa labor artística. Hasta que en 1965 abre sus puertas la Escuela
                        Nacional de Danza Moderna y Folklórica, dentro del Sistema Nacional de Enseñanza
                        Artística. A partir de Ramiro Guerra surgieron artistas que comenzaron como
                        bailarines y se convirtieron en importantes creadores; entre ellos se destaca
                        Eduardo Rivero. Eduardo Rivero logra un aporte sustancial y contundente a la
                        Escuela Nacional de Danza Moderna. Las ondulación del torso, de los brazos y
                        otras peculiaridades, pasan al Método de la forma de enseñanza, viene desde la
                        época de Ramiro Guerra y se consolida. Es de los elementos que distingue a la
                        técnica cubana del resto.
                    </p>
