                <p>
                    La Escuela De Danzas Proyecto Mestizo es un espacio dedicado a la enseñanza de las danzas populares y técnicas,
                    entendidas como lugares de encuentro y de construcción de herramientas y mixturas para el trabajo artístico y
                    la construcción de identidad.
                    <br><br>
                    Creada por Gaby Fabro en el año 2010, espacio donde dicta clases y talleres de la Técnica Afro Contemporánea
                    y se constituye en la sede de la compañía homónima.
                    <br><br>
                    Con el lema "abocados a destacar las raíces negras de Latinoamérica", buscamos incorporar la técnica a la
                    preparación básica del cuerpo, la estética y la filosofía de movimiento que las danzas folklóricas y populares
                    de América Latina y el Caribe nos ofrecen.
                    <br><br>
                    Creemos que la disciplina de las danzas técnicas y lo visceral de las danzas populares generan la sinergia
                    perfecta que nos llevará a formar bailarines con calidad y naturalidad en la interpretación.
